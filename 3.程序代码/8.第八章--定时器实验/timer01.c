#include<reg52.h>

#define FOSC 11059200 //单片机晶振频率
#define T_1ms (65536 - FOSC/12/1000)  //定时器初始值计算

sbit led0 = P1^0;
unsigned char count = 0;
void main()
{
	TMOD = 0x01;	 //定时器工作模式配置
	TL0  = T_1ms;	//装载初始值
	TH0  = T_1ms>>8;

	TR0  = 1;		 //启动定时器
	while(1)
	{
		if(TF0==1)//判断1ms定时是否到达，到了则进入
		{
			TF0 = 0; //首先进行清零，为下一次做准备

			TL0 = T_1ms;//重装初始值
			TH0 = T_1ms>>8;	

			count++;
			if(count>=50)//	每一毫秒进入一次中断，达到50次则为50ms,翻转小灯。
			{
				count = 0;
				led0 = ~led0;
			}								
		}
	}
}
