#include<reg52.h>	//寄存器声明头文件
sbit led0 = P1^0; // 位声明，将P1.0管脚声明为led0 

void main()	   //程序主函数入口，每个C语言程序有且只有一个
{
	while(1) //循环
	{
		led0 = 0;	//赋值管脚P1.0为低电平，点亮LED小灯
	}
}